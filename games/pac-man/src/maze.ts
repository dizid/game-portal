export interface TileGrid {
  data: number[][]
}

/** Deep-clone the template so each game gets fresh dot state */
export function createMaze(): TileGrid {
  return { data: MAZE_TEMPLATE.map(row => [...row]) }
}

export function isWall(grid: TileGrid, col: number, row: number): boolean {
  if (col < 0 || col >= COLS || row < 0 || row >= ROWS) return true
  return grid.data[row][col] === T_WALL
}

export function isDoor(grid: TileGrid, col: number, row: number): boolean {
  if (col < 0 || col >= COLS || row < 0 || row >= ROWS) return false
  return grid.data[row][col] === T_DOOR
}

export function countDots(grid: TileGrid): number {
  let count = 0
  for (const row of grid.data) {
    for (const cell of row) {
      if (cell === T_DOT || cell === T_PELLET) count++
    }
  }
  return count
}
