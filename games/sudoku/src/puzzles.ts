/** Pick a random puzzle of given difficulty */
export function pickPuzzle(difficulty: Difficulty): Puzzle {
  const pool = PUZZLES.filter((p) => p.difficulty === difficulty)
  return pool[Math.floor(Math.random() * pool.length)]
}
